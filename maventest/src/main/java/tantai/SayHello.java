public class SayHello {
/**
* isTrue.
* @param condition is condition
*/
    public final void isTrue(final String condition) {
        if (condition.compareTo("true") == 0) {
            System.out.println("is true");
        } else {
            System.out.println("is false");
        }
    }
}
